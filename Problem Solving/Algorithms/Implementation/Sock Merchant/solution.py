# Complete the sockMerchant function below.
def sockMerchant(n, ar):
    count = 0
    ar_set = set(ar)
    for i in ar_set:
        count += ar.count(i) % 2
    return (n-count)//2

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input())

    ar = list(map(int, input().rstrip().split()))

    result = sockMerchant(n, ar)

    fptr.write(str(result) + '\n')

    fptr.close()
