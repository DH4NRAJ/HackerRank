import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {

    Scanner s = new Scanner(System.in);
    int count=0;
    while (s.hasNext()) {
      count++;
      System.out.println(count+" "+s.nextLine());
    }
    s.close();
    }
}
